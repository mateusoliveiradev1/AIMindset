## Conceitos Fundamentais

### Overfitting vs Underfitting
- **Overfitting**: Modelo muito complexo, memoriza dados de treino
- **Underfitting**: Modelo muito simples, não captura padrões
- **Solução**: Validação cruzada e regularização

### Bias vs Variance
- **Bias**: Erro sistemático do modelo
- **Variance**: Sensibilidade a mudanças nos dados
- **Trade-off**: Equilibrar ambos para melhor performance

### Feature Engineering
Criar novas variáveis a partir das existentes:
- Combinações de features
- Transformações matemáticas
- Codificação de variáveis categóricas

